<h1>FAQ</h1>

<div class="content-inner">
<div class="questions">
<div class="question">
  <p>
    <span>Q. My child is ____ years old. What do you have to offer for their age group?</span>
  </p>

  <p>
    During the fall and spring semesters we have production classes (ages 5 & up) and preschool creative
    drama
    classes (ages 3 & 4). We also have camps for ages 5 & up during Fall Break, Winter Break, and Spring
    Break. In the summer, we have 5-day theatre camps each week starting the day after Memorial Day and
    ending
    mid-August. We also have many specialty summer camps to choose from each week, like dance, musical
    theatre, filmmaking, and magic. All of our classes are divided strictly by age—level 1 is for 5-7 year
    olds, level 2 is for 8-12 year olds, level 3 is for 11-13 year olds, and preschool level is for 3 & 4
    year
    olds.
  </p>
</div>

<div class="question">
  <p>
    <span> Q. What are the times of your classes & camps?</span>
  </p>

  <p>
    School year:
    Please check the classes tab on the website for specific dates and times for classes.
    Seasonal camps and summer camps:
    Preschool summer camps are 9am-12pm Monday through Friday, with a performance on Friday at 11:30. All
    other camps are 9am-4pm, Monday through Friday. Performances begin each Friday at 4pm. Before and After
    Care is available 8am-6pm for an additional charge. There is no After Care on Friday due to
    performances.
  </p>
</div>

<div class="question">
  <p>
    <span> Q. How do I register for classes or camps?</span>
  </p>

  <p>
    Register by visiting our website, <a href="www.oklahomachildrenstheatre.org"> www.oklahomachildrenstheatre.org</a>. You can also enroll by calling
    405/606-7003. You may also print an enrollment form from our website and fax or mail it to our office, but
    it’s a good idea to call first and check the availability of the camps that you’re interested in to make
    sure they’re not yet full. Full payment is required to reserve spots in camps and classes.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. The class/camp I wanted to enroll my child in is full. Can I be put on a waiting list?</span>
  </p>

  <p>
    Absolutely. We have waiting lists for all camps that fill. Just ask!
  </p>
</div>

<div class="question">
  <p>
    <span>Q. What forms of payment do you accept?</span>
  </p>

  <p>
    Visa, Mastercard, Discover, check, or cash. We must have payment before your spot can be saved in a class
    or camp.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. Are there any additional fees for your classes/camps?</span>
  </p>

  <p>
    There are no extra required fees outside of your tuition payment. For camps, parents will have an option
    to purchase a Camp Store Card for their camper to spend at the camp store, and we will also have an
    optional $5 all-you-can-eat pizza or hot dog party on each Friday of camp. Before and Aftercare (8am-6pm)
    is also available for an extra charge.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. Do I have to pay the entire tuition when I enroll or do I pay a deposit?</span>
  </p>

  <p>
    In order to reserve your child’s spot in a class or camp, we must have your full tuition payment.
    Exceptions are made only for campers enrolling in ten or more weeks of summer camps, in which case half
    tuition is paid upon enrollment and the other half paid on July 1st.
  </p>
</div>

<div class="question">
  <p>
    <span> Q. Can I reserve a spot in camp for my child and pay later?</span>
  </p>

  <p>

    Our classes and camps fill very quickly, so full payment is required in order to reserve spots.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. How do you handle safety and security?</span>
  </p>

  <p>
    Our building and grounds are patrolled by the Oklahoma City University Police Department. The OCUPD is
    also our first responder in case of emergencies. We have surveillance cameras installed at the front and
    back doors of our building, and our outside play area is monitored by no less than two teachers. All of
    our teachers and assistants must clear an OSBI Criminal History Background Check, an Oklahoma DOC Sex
    Offender Check, and an Oklahoma DOC Violent Offender Check before they are allowed to work. Also, we
    always have a teacher or staff person on duty that is certified in CPR. We use a numerical code system for
    checking out campers. Adults picking up children must provide an assigned numerical code in order check
    out their child. If you have additional questions about safety and security, please call the office.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. What is the class size of Children’s Theatre camps?</span>
  </p>

  <p>
    Our preschool class limit is 10-12 students with one teacher and one assistant teacher.
    Our class limit is 15-18 with one teacher and one assistant teacher. If a 5-7 year old class fills with
    more than 16, they will have an additional assistant placed with the class.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. Who are your teachers?</span>
  </p>

  <p>
    Our teachers are local professionals in their fields or upperclassmen OCU theatre, musical theatre, or
    education students, and all have teaching experience. Our assistants are Oklahoma City University theatre
    students, and some are veteran students of OCT themselves.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. My 7-year-old will be turning 8 very soon. Can I enroll them in the 8-12 year old class?</span>
  </p>

  <p>
    In order to enroll in the level 1 (5-7 year old) class, your child must have their 5th birthday before or
    during the camp dates for which you are registering. Likewise for the 8-12 class, your child must have
    their 8th birthday before or during the camp dates for which you are registering.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. Does my child need previous experience to take any of your camps?</span>
  </p>

  <p>
    Not at all! All of our classes welcome beginners. At the same time, no two classes are exactly alike, so
    campers can take multiple classes and camps and always have new experiences. The only camps that require
    previous experience are our two and three week production camps in the summer which also require an
    audition.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. Will my child’s camp have a performance?</span>
  </p>

  <p>
    Yes! All of our camps and classes end in a performance. Our semester classes will have performances
    following their last class, and all camps have performances on the Friday of camp. Magic campers perform
    their magic tricks in a magic show, dance campers perform their dance numbers, theatre campers perform the
    plays they’ve developed, and so on. Performances begin at 4pm on Fridays and all family and friends are
    welcome free of charge. The only exceptions are for our Filmmaking and Video Game Design camps, which do
    not have a performance but have a final screening of their film or a showcase of their video game design
    work.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. My child is pretty shy. What if they have stage fright?</span>
  </p>

  <p>
    At Oklahoma Children’s Theatre, we focus on teaching self-expression and the process of theatre and
    acting; we keep performance pressure at very low priority. We encourage all of our campers to express
    themselves in their own way, whether that is acting, designing costumes, helping backstage, or any other
    interest in theatre arts that your child may have. Our teachers are very understanding, comforting, and
    reassuring, and while we will encourage all campers to express themselves, no camper is forced to do
    anything that would make them uncomfortable.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. Can I drop in and observe my child’s class?</span>
  </p>

  <p>
    Our camps and classes are Kids Only. We’ve found that our students and teachers are more at ease when they
    don’t feel the pressure to perform for parents and onlookers. Our summer camp teachers submit daily camp
    reports Monday through Thursday that detail the activities of their classes. If you’d like to talk about
    what your child’s class is up to, please call our camp director at 405/606-7003 or 405/620-2875.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. This is my first time enrolling. If my child doesn’t enjoy it, can I get a refund?</span>
  </p>

  <p>
    Oklahoma Children’s Theatre staff and teachers do our best to ensure that all of our campers have an
    enjoyable experience, regardless of their experience level, age, ability, or interest. We do not offer
    refunds after a camp has begun, but we can offer you a receipt that will allow you to claim the unused
    portion of your tuition as a tax deductable donation. You can receive a full refund minus a $10 processing
    fee per camp if you withdraw from camp five days prior to the camp beginning.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. Where are your camps located?</span>
  </p>

  <p>
    We are located on campus at Oklahoma City University, just off of 23rd street between Penn and Classen.
    Our main facility is the Children’s Center for the Arts building, and classes also take place in the
    Kirkpatrick Fine Arts Center, the Wanda L. Bass School of Music, the Kramer Nursing School building, and
    Wesley United Methodist Church building. Our campers are always chaperoned by a teacher and/or assistant.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. What does my camper need to bring each day?</span>
  </p>

  <p>
    Classes: If you child receives a script as part of their class, please bring it to each class until they
    have all their lines memorized or until the teacher is done with script work.
    Camp: Each day campers will need two snacks, a sack lunch, and drinks for the day. You can pack all of
    these, or you can pack a sack lunch and purchase a Camp Store Card that your camper can use to buy snacks
    at the camp store.
    Both: Unless specifically asked by a teacher, all costumes and props are provided by OCT.
  </p>
</div>

<div class="question">
  <p>
    <span>Q. What else do I need to know?</span>
  </p>

  <p>
    We know parents have lots of questions, and we’re here to answer them! Our Parent Packet may answer many
    of your questions. Parent Packets will be emailed one week prior to the start of your child’s class or
    camp. You can also call 405/606-7003 and speak to a Children’s Theatre staff person that can help you with
    any questions you may have.
  </p>
</div>
