            <p>
              <strong>Performances</strong>
              <span>Season:</span>
              Our regular season consists of 8 productions – 7 geared for Pre-K thru 5th grade, 1 for 5th grade
              and up. Each production is performed in one of three performance spaces on the OCU campus. We do
              approximately 70 performances per season, including both daytime weekday field trip performances
              and weekend performances for families.</p>

            <p><span>Co-Productions with TheatreOCU:</span>
              We normally do 1 co-production per season; this show is usually more mature and are geared for 5th
              grade and up. These are part of our regular season as well as part of the TheatreOCU season, and
              roles are cast primarily with OCU students.</p>

            <p><span>Touring:</span>
              We do one touring production each year. It may or may not be included as one of the regular season
              shows. We tour throughout the state to approximately 25 - 30 different communities, primarily
              communities that are too far away to make a field trip.</p>

            <p><strong>Education</strong>
              <span>Camps:</span>
              We host one-week camps for 11-12 weeks in the summer and during all seasonal school breaks
              (spring, fall, and winter). The camp season includes over 78 camps and 1400 campers altogether.
              Camps for ages 3-4, 5-7, 8-12, and 10-13 are offered in a diverse range of creative and performing
              arts.</p>

            <p><span>3-week production camps:</span>
              Summer camps for more advanced students; these camps end in a series of ticketed performances that
              are part of the regular production season.</p>

            <p><span>Classes:</span>
              Creative Drama for 3 & 4 year olds, and Play Production classes for ages 5-7 and 8-12 are offered
              in the spring and fall semesters. All classes end with friends & family performances. Production
              classes include the production process from auditions and line memorization to costumes, lights
              and performances.</p>

            <p><span>Young Company:</span>
              Our highest level of education for the most seasoned young performers. Young Company have
              workshops and lessons as well as a variety of performances at community events such as OKC
              Philharmonic concerts, Arts Fest, Fairy Tale Ball, and TheatreOCU/Children’s Theatre
              co-productions.</p>

            <p><strong>Outreach</strong>
              In-school residency programs:
              Our educational outreach residencies are housed in Putnam Heights and Edgemere and Cleveland
              schools. Each semester we host up to four classes at each location and teach theatre skills as
              well as creative thinking and teamwork through drama education. Each class enjoys the thrill of a
              final performance for their friends and family. Educational outreach programs are provided at no
              or minimal cost to the school.</p>

            <p><span>Page to Stage:</span>
              Page to Stage is a traveling, short-term workshop series for Title I schools that focuses on
              literacy, comprehension, and “reading for meaning.” Each participating class has three workshops
              in their classroom with an OCT teaching artist that focus on vocabulary and plot using a well
              known story. A production of that story is then brought to the school, and finally each student in
              the class receives a copy of the book on which the play was based. The entire Page to Stage
              experience is made possible by OCT program sponsors and is free to the school.</p>

            <p>
              <span>Tickets and transportation facilitation:</span>
              From the bus and ticket packages purchased at Fairy Tale Ball, we call the school that the donor
              designated as the beneficiary and make arrangements and reservations with the school and OCT box
              office. If no school is designated by the donor, we scout for Title 1 schools that are age and
              travel appropriate for the donated package.
            </p>

            <p>
              <strong>Fundraising</strong>
              Fairy Tale Ball: Our annual fundraising event is a one-of-a-kind gala in OKC. It's a dazzling, fun
              and fantasy filled evening for the whole family! Imaging spinning through the ground level doors
              of hte chase Tower into a world filled with games and play areas for adults and children.
              Everywhere you look you see all your favorite princesses, knights in shining armor, dragons,
              fairies, princes, kings and queens along with many more storybook characters. Formal dinner for
              the adults follows, along with dinner and entertainment events for both children and tweens. The
              whole family rejoins at the end of the night for an unforgettable fairytale dance party.
            </p>

            <p>
              <strong>Mentoring</strong>
              OCU students: Each semester we employ up to seven part time student workers from the OCU campus.
              It’s great for OCT to have so many extra hands, and it also provides these students with practical
              education and experience in working for a professional theatre company. Our student workers have
              the opportunity to learn teaching skills as well as gain experience in box office management,
              props construction, PR, and many other areas that will benefit them when they graduate and are
              employed by a theatre companies elsewhere. We have also started a series of educational staff
              meetings during which the students are presented with lessons that will benefit their theatre
              education, for instance, a lesson on what it means to have non-profit status, how to plan an
              education season, etc.
            </p>

            <p>
              <span>Youth Board:</span>
              New for 2013! The Youth Board consists of seasoned children’s theatre kids, ages 13 and up, who have
              volunteered, performed and/or worked with us as they’ve grown. This group’s goals include fundraising,
              volunteering and creatively enhancing OCT’s programming in the community.
            </p>

            <p>
              <span>Teen Volunteers:</span>
              Our teen volunteers are crucial during our seasonal camps and summer camp months, helping with a variety
              of tasks like stocking and running the Camp Store, creating the weekly programs, assisting different
              classes and Fun Time hours, escorting late/early campers to and from classes, and assisting the before and
              aftercare teachers. They also help with a variety of other tasks that are assigned as needed, during the
              summer and throughout the school year. Many of our volunteers have been campers for many years, and aspire
              to become teachers’ assistants when they are old enough.
            </p>
