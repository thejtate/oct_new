            as
            Executive Director.</p>

          <p>
            Ms. Adams operated a resident theatre program at Oklahoma City Community College from December 1986 to
            August
            1989. During these three years, Oklahoma Children's Theatre audience grew from 10,000 to over 20,000.</p>

          <p>
            In this same period, the theatre implemented a number of additional programs including: Summer Children's
            Theatre Workshops, Children's Theatre Tour of the Putnam City Schools and a Youth Conservatory Program. In
            August 1989, Oklahoma Children's Theatre was invited to become a resident at City Arts Center, on the
            fairgrounds.</p>

          <p>
            This move permitted the expandability of productions and educational programs for young audiences to
            include:
            main stage productions each year, a statewide touring program, a youth theatre program and an arts
            collaboration program between Oklahoma Children's Theatre, local daycare centers and community artists.</p>

          <p>
            In September of 2005, OCT moved to the Campus of Oklahoma City University, where a very successful
            collaborations in both performance and educational programming has enabled even more growth.
          </p>
