            In August of 2007 OCT moved office spaces one and half blocks down the road to a building owned by OCU on
            the
            corner of Blackwelder and 25th street. The building has always been known as the Children's Center for the
            Arts and was the original home of Claire Jones Children's Theatre program. Through significant financial
            support from the Inasmuch Foundation, it has been transformed into a warm, exciting, versatile, fully
            functional theatre space for young audiences. Classes and performances are regularly held in the CCA. Fall,
            winter, spring and summer camps are focused in the space as well as before and after care and home school
            programs.
          </p>

          <p>
