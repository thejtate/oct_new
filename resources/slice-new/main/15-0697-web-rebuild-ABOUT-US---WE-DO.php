              classroom with an OCT teaching artist that focus on vocabulary and plot using a well known story. A
              production of that story is then brought to the school, and finally each student in the class receives a
              copy of the book on which the play was based. The entire Page to Stage experience is made possible by
              OCT program sponsors and is free to the school.
            </p>
