                      <h4>Our History</h4>
                    </div>

                    <div class="cols cols-2 field field-name-field-page-content-col field-type-text-long field-label-hidden">
                      <div class="field-items">
                        <div class="col col-1"><p><strong>Oklahoma Children's Theatre</strong> began as a program of the Stage Center in downtown Oklahoma City. The program was a successful revenue generating program meeting the needs of children and youth audiences.</p>
                          <div><img alt="" src="/sites/default/files/img-history-1.png" style="float:left"></div>
                          <p>In December 1986, the theatre became an independent non-profit organization with founder, <strong>Lyn Adams</strong>, named as Executive Director.</p>
                          <p>Ms. Adams operated a resident theatre program at Oklahoma City Community College from December 1986 to August 1989. During these three years, Oklahoma Children's Theatre audience grew from 10,000 to over 20,000.</p>
                          <p>In this same period, the theatre implemented a number of additional programs including: Summer Children's Theatre Workshops, Children's Theatre Tour of the Putnam City Schools and a Youth Conservatory Program. In August 1989, Oklahoma Children's Theatre was invited to become a resident at City Arts Center, on the fairgrounds.</p>
                          <p>This move permitted the expandability of productions and educational programs for young audiences to include: main stage productions each year, a statewide touring program, a youth theatre program and an arts collaboration program between Oklahoma Children's Theatre, local daycare centers and community artists.</p>
                          <p>In September of 2005, OCT moved to the Campus of Oklahoma City University, where a very successful collaborations in both performance and educational programming has enabled even more growth.</p>
                        </div>
                        <div class="col col-2"><div><img src="/sites/default/files/img-history-2.png"></div>
                          <p>In August of 2007 OCT moved office spaces one and half blocks down the road to a building owned by OCU on the corner of Blackwelder and 25th street. The building has always been known as the Children's Center for the Arts and was the original home of Claire Jones Children's Theatre program. Through significant financial support from the Inasmuch Foundation, it has been transformed into a warm, exciting, versatile, fully functional theatre space for young audiences. Classes and performances are regularly held in the CCA. Fall, winter, spring and summer camps are focused in the space as well as before and after care and home school programs.</p>
                          <p>OCT offers annually 8 in house and 2 statewide touring performances, weekday and weekend after school classes as well as summer, fall, winter, spring camps and teacher/student workshops and after school programming to underserved elementary schools. Estimated annual attendance is approximately 42,500 for all activities. OCU provides space for all OCT programs and in return OCT works with students as performers, administrative support, assistants and more whenever applicable.</p>
